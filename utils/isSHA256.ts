export const isSHA256 = (str: string) => {
  const regexExp = /^[a-f0-9]{64}$/gi;
  return regexExp.test(str);
};

console.log(isSHA256('60F1CF097F7D1EDC78CA9D33C858BC47174D5FD45A6D5190850EB1D742709DC3'));
